#include <stdio.h>
#include "main.h"

/**
 * print_alphabet_x10 - Print the lowercase alphabet 10 times,
 * each followed by a new line
*/

void print_alphabet_x10(void)
{
	char ch;
	int count;

	for (ch = 0; ch < 10; count++)
	{

	for (ch = 'a'; ch <= 'z'; ch++)
	{

	_putchar('ch');

	}
	_putchar('/n');
	}
	}
