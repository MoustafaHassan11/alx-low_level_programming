#include "main.h"

/**
 * reset_to_98 - Update the value pointed to by a pointer to 98.
 *
 * This function takes a pointer to an integer as a parameter and
 * modifies the value at the memory location pointed to by the pointer
 * to be 98.
 *
 * @n: A pointer to an integer that should be updated to 98.
 */


void reset_to_98(int *n)
{

	*n = 98;

}
